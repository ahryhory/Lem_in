#include "lem_in.h"

void	free_matr(int ***p_matr)
{
	int		**matr;
	int		i;

	matr = *p_matr;
	i = 0;
	while (matr[i][0] != -1)
	{
		free(matr[i]);
		i++;
	}
	free(matr[i]);
	free(matr);
}
